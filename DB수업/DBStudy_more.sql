-- 전직원중 제일 급여를 많이 받는 직원 상위 5명만 출력해주세요!
CREATE VIEW BESTSAL AS
SELECT SALARY, FIRST_NAME 
FROM EMPLOYEES
ORDER BY SALARY DESC NULLS LAST;

SELECT *
FROM BESTSAL
