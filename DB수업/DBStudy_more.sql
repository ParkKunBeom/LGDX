
CREATE VIEW 부서별최고급여 AS
SELECT EMPLOYEE_ID, FIRST_NAME, SALARY, DEPARTMENT_ID
FROM EMPLOYEES
WHERE (SALARY, DEPARTMENT_ID) IN (SELECT MAX(SALARY), DEPARTMENT_ID
                                  FROM EMPLOYEES
                                  GROUP BY DEPARTMENT_ID);

SELECT * FROM 부서별최고급여;                                  
-- VIEW : 가상의 테이블
-- 복잡한 SELECT 문을 저장하기 위해 사용한다
SELECT EMPLOYEE_ID, SALARY, ms.DEPARTMENT_ID, CITY
FROM LOCATIONS l, 부서별최고급여 ms, DEPARTMENTS d
WHERE ms.DEPARTMENT_ID=d.DEPARTMENT_ID AND d.LOCATION_ID=l.LOCATION_ID;

-- 각부서별 부서번호, 부서명, 도시명, 나라이름을 가지는 VIEW(DEPARTVIEW)를 만들어주세요
-- 각 도시별 몇명이 근무하는지 출력해주세요
CREATE VIEW DEPARTVIEW AS
SELECT DEPARTMENT_ID, DEPARTMENT_NAME, CITY, COUNTRY_NAME
FROM DEPARTMENTS d, LOCATIONS l, COUNTRIES c
WHERE d.LOCATION_ID=l.LOCATION_ID AND l.COUNTRY_ID=c.COUNTRY_ID;

SELECT COUNT(e.EMPLOYEE_ID) AS "인원수", di.CITY
FROM DEPARTVIEW di, EMPLOYEES e
WHERE di.DEPARTMENT_ID=e.DEPARTMENT_ID
GROUP BY CITY; 


